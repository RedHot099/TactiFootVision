#!/usr/bin/env python3
"""Generate quick accuracy and confusion plots for the team classification experiment."""
from __future__ import annotations

import argparse
from pathlib import Path
from typing import Sequence

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt  # noqa: E402
import numpy as np  # noqa: E402
import pandas as pd  # noqa: E402


def load_args(argv: Sequence[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Plot metrics for team classification experiment.")
    default_dir = Path(__file__).resolve().parent
    parser.add_argument(
        "--metrics",
        type=Path,
        default=default_dir / "metrics.csv",
        help="Path to metrics CSV generated by run_team_classification_experiment.py",
    )
    parser.add_argument(
        "--predictions",
        type=Path,
        default=default_dir / "predictions.csv",
        help="Path to per-crop predictions CSV.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=default_dir,
        help="Directory to store generated plots.",
    )
    return parser.parse_args(argv)


def plot_accuracy(metrics: pd.DataFrame, output_dir: Path) -> Path:
    labels = []
    acc_values: list[float] = []
    colors: list[str] = []
    for _, row in metrics.iterrows():
        labels.append(f"{row['experiment_stage']}: {int(row['umap_components'])}")
        acc_values.append(float(row["accuracy"]))
        colors.append("#1f77b4" if row["experiment_stage"] == "ratio_sweep" else "#ff7f0e")

    positions = np.arange(len(labels))
    fig, ax = plt.subplots(figsize=(6, 4))
    ax.bar(positions, acc_values, color=colors)
    ax.set_ylim(0, 1)
    ax.set_ylabel("Accuracy")
    ax.set_title("Accuracy per configuration")
    ax.set_xticks(positions)
    ax.set_xticklabels(labels, rotation=20, ha="right")
    for idx, val in enumerate(acc_values):
        ax.text(idx, val + 0.01, f"{val:.3f}", ha="center", va="bottom", fontsize=8)
    fig.tight_layout()
    output_path = output_dir / "accuracy.png"
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return output_path


def plot_confusion(preds: pd.DataFrame, output_dir: Path) -> Path:
    true = preds["true_label"].astype(int)
    pred = preds["predicted_label"].astype(int)
    unique_labels = sorted(set(true) | set(pred))
    label_to_idx = {label: idx for idx, label in enumerate(unique_labels)}
    cm = np.zeros((len(unique_labels), len(unique_labels)), dtype=int)
    for t, p in zip(true, pred):
        cm[label_to_idx[t], label_to_idx[p]] += 1

    fig, ax = plt.subplots(figsize=(4, 4))
    im = ax.imshow(cm, cmap="Blues")
    ax.set_xticks(range(len(unique_labels)))
    ax.set_xticklabels(unique_labels)
    ax.set_yticks(range(len(unique_labels)))
    ax.set_yticklabels(unique_labels)
    ax.set_xlabel("Predicted")
    ax.set_ylabel("True")
    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            ax.text(j, i, str(cm[i, j]), ha="center", va="center", color="black", fontsize=9)
    fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    ax.set_title("Confusion matrix (all samples)")
    fig.tight_layout()
    output_path = output_dir / "confusion.png"
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return output_path


def plot_comparative_accuracy(metrics: pd.DataFrame, output_dir: Path) -> Path:
    if "umap_applied" not in metrics.columns:
        raise ValueError("Metrics CSV lacks 'umap_applied' column required for comparative plot.")

    # Prepare data: set umap_value=0 for rows without UMAP to align baselines.
    data = metrics.copy()
    data["umap_value"] = np.where(data["umap_applied"], data["umap_components"], 0).astype(int)

    # Aggregate per color/method/ratio/umap_value (mean accuracy over sequences if multiple).
    grouped = (
        data.groupby(
            ["color_space", "crop_method", "crop_center_ratio", "umap_value"],
            as_index=False,
        )["accuracy"]
        .mean()
    )

    methods = ["center", "opencv_mask", "sam2_mask"]
    method_colors = {
        "center": "#1f77b4",
        "opencv_mask": "#2ca02c",
        "sam2_mask": "#d62728",
    }
    linestyles = ["solid", "dashed", "dotted", "dashdot"]

    color_spaces = sorted(grouped["color_space"].unique())
    fig, axes = plt.subplots(1, len(color_spaces), figsize=(6 * max(1, len(color_spaces)), 4), squeeze=False)

    for ax, color_space in zip(axes.ravel(), color_spaces):
        subset = grouped[grouped["color_space"] == color_space]
        if subset.empty:
            ax.set_visible(False)
            continue
        ratios = sorted(subset["crop_center_ratio"].unique())
        for idx, ratio in enumerate(ratios):
            ratio_rows = subset[subset["crop_center_ratio"] == ratio]
            for method in methods:
                method_rows = ratio_rows[ratio_rows["crop_method"] == method]
                if method_rows.empty:
                    continue
                # Sort by umap_value for consistent lines.
                method_rows = method_rows.sort_values("umap_value")
                ax.plot(
                    method_rows["umap_value"],
                    method_rows["accuracy"],
                    label=f"{method} | r={ratio:.2f}",
                    color=method_colors.get(method, "#777777"),
                    linestyle=linestyles[idx % len(linestyles)],
                    marker="o",
                    markersize=3,
                    alpha=0.9,
                )
        ax.set_title(f"Color space: {color_space}")
        ax.set_xlabel("UMAP components (0 = no UMAP)")
        ax.set_ylabel("Accuracy")
        ax.set_ylim(0, 1)
        ax.grid(True, linestyle=":", linewidth=0.5, alpha=0.6)
        ax.legend(fontsize=8, loc="lower right")

    fig.tight_layout()
    output_path = output_dir / "comparative_accuracy.png"
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return output_path


def plot_umap_vs_baseline(metrics: pd.DataFrame, output_dir: Path) -> Path:
    if "umap_applied" not in metrics.columns:
        raise ValueError("Metrics CSV lacks 'umap_applied' column required for UMAP comparison plot.")

    data = metrics.copy()
    # Split baseline (no UMAP) vs UMAP and take best accuracy per group.
    baseline = (
        data[~data["umap_applied"]]
        .groupby(["color_space", "crop_method", "crop_center_ratio"], as_index=False)["accuracy"]
        .max()
        .rename(columns={"accuracy": "baseline_acc"})
    )
    with_umap = (
        data[data["umap_applied"]]
        .groupby(["color_space", "crop_method", "crop_center_ratio"], as_index=False)["accuracy"]
        .max()
        .rename(columns={"accuracy": "umap_acc"})
    )
    merged = pd.merge(baseline, with_umap, on=["color_space", "crop_method", "crop_center_ratio"], how="inner")
    if merged.empty:
        raise ValueError("No matching baseline and UMAP rows to compare.")

    merged["label"] = merged.apply(
        lambda r: f"{r['color_space']}/{r['crop_method']}|r={r['crop_center_ratio']:.2f}", axis=1
    )

    positions = np.arange(len(merged))
    width = 0.35
    fig, ax = plt.subplots(figsize=(max(8, len(merged) * 0.6), 5))
    ax.bar(positions - width / 2, merged["baseline_acc"], width, label="No UMAP", color="#1f77b4")
    ax.bar(positions + width / 2, merged["umap_acc"], width, label="Best UMAP", color="#ff7f0e")
    ax.set_xticks(positions)
    ax.set_xticklabels(merged["label"], rotation=25, ha="right")
    ax.set_ylim(0, 1)
    ax.set_ylabel("Accuracy")
    ax.set_title("UMAP vs No UMAP (best per group)")
    for i, (b, u) in enumerate(zip(merged["baseline_acc"], merged["umap_acc"])):
        ax.text(i - width / 2, b + 0.01, f"{b:.3f}", ha="center", va="bottom", fontsize=8)
        ax.text(i + width / 2, u + 0.01, f"{u:.3f}", ha="center", va="bottom", fontsize=8)
    ax.legend()
    fig.tight_layout()
    output_path = output_dir / "umap_vs_baseline.png"
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return output_path


def plot_method_comparison(metrics: pd.DataFrame, output_dir: Path) -> Path:
    # Take best accuracy per method/color across ratios and UMAP settings.
    grouped = (
        metrics.groupby(["color_space", "crop_method"], as_index=False)["accuracy"]
        .max()
        .rename(columns={"accuracy": "best_accuracy"})
    )
    if grouped.empty:
        raise ValueError("Metrics CSV is empty; cannot plot method comparison.")

    color_spaces = sorted(grouped["color_space"].unique())
    fig, axes = plt.subplots(1, len(color_spaces), figsize=(5 * max(1, len(color_spaces)), 4), squeeze=False)
    method_order = ["center", "opencv_mask", "sam2_mask"]
    method_colors = {
        "center": "#1f77b4",
        "opencv_mask": "#2ca02c",
        "sam2_mask": "#d62728",
    }
    for ax, color_space in zip(axes.ravel(), color_spaces):
        subset = grouped[grouped["color_space"] == color_space]
        if subset.empty:
            ax.set_visible(False)
            continue
        subset = subset.set_index("crop_method").reindex(method_order).dropna(subset=["best_accuracy"])
        methods = subset.index.tolist()
        positions = np.arange(len(methods))
        accs = subset["best_accuracy"].tolist()
        colors = [method_colors.get(m, "#777777") for m in methods]
        ax.bar(positions, accs, color=colors)
        ax.set_xticks(positions)
        ax.set_xticklabels(methods, rotation=15, ha="right")
        ax.set_ylim(0, 1)
        ax.set_ylabel("Accuracy")
        ax.set_title(f"Best per method | color={color_space}")
        for i, val in enumerate(accs):
            ax.text(i, val + 0.01, f"{val:.3f}", ha="center", va="bottom", fontsize=8)
        ax.grid(True, axis="y", linestyle=":", linewidth=0.5, alpha=0.6)

    fig.tight_layout()
    output_path = output_dir / "methods_comparison.png"
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return output_path


def main(argv: Sequence[str] | None = None) -> None:
    args = load_args(argv)
    metrics_path = args.metrics.expanduser().resolve()
    preds_path = args.predictions.expanduser().resolve()
    output_dir = args.output_dir.expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    if not metrics_path.is_file():
        raise FileNotFoundError(f"Metrics CSV not found: {metrics_path}")
    if not preds_path.is_file():
        raise FileNotFoundError(f"Predictions CSV not found: {preds_path}")

    metrics = pd.read_csv(metrics_path)
    preds = pd.read_csv(preds_path)

    acc_path = plot_accuracy(metrics, output_dir)
    cm_path = plot_confusion(preds, output_dir)
    adv_path = plot_comparative_accuracy(metrics, output_dir)
    umap_path = plot_umap_vs_baseline(metrics, output_dir)
    method_path = plot_method_comparison(metrics, output_dir)
    print(f"Saved accuracy plot to {acc_path}")
    print(f"Saved confusion matrix to {cm_path}")
    print(f"Saved comparative plot to {adv_path}")
    print(f"Saved UMAP vs baseline plot to {umap_path}")
    print(f"Saved method comparison plot to {method_path}")


if __name__ == "__main__":
    main()
